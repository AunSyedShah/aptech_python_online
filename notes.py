# string is represented as a list of characters and is written inside double qoutes
name = "Hello World"
# numbers are written without quotes
age = 20
# list is written inside square brackets and values are comma seperated
numbers = [1, 2, 3, 4, 5]
# tuple is written inside round brackets and values are comma seperated
# tuple is immutable
marks = (10, 20, 30, 40, 50)
# dictionary is written inside curly brackets and values are comma seperated
# dictionary is mutable
student = {"name": "John", "age": 20}
